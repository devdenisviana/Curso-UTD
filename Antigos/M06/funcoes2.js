// Criando um objeto chamado aluno com três propriedades: nome, idade e curso
let aluno = {
    nome: 'Anita', // Definindo a propriedade nome com o valor 'Anita'
    idade: 22,     // Definindo a propriedade idade com o valor 22
    curso: 'Engenharia de Produção' // Definindo a propriedade curso com o valor 'Engenharia de Produção'
}

// Imprimindo o valor da propriedade nome do objeto aluno
console.log(aluno.nome); // Saída: Anita

// Imprimindo o valor da propriedade idade do objeto aluno
console.log(aluno.idade); // Saída: 22

// Imprimindo o valor da propriedade curso do objeto aluno
console.log(aluno.curso); // Saída: Engenharia de Produção

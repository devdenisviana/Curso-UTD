// Declaração de uma variável chamada "lista" e inicialização como um array vazio.
let lista = [];

// Adiciona elementos ao array "lista" em posições específicas.
lista[0] = "leite";    // Adiciona "leite" na posição 0 do array.
lista[1] = "carne";    // Adiciona "carne" na posição 1 do array.
lista[2] = "frango";   // Adiciona "frango" na posição 2 do array.
lista[3] = "maçã";     // Adiciona "maçã" na posição 3 do array.
lista[4] = "abacate";  // Adiciona "abacate" na posição 4 do array.
lista[5] = "peixe";    // Adiciona "peixe" na posição 5 do array.


// Adiciona o elemento "uva" ao final do array "lista".
lista.push("uva");

// Remove o último elemento do array "lista".
lista.pop();

// Adiciona o elemento "aveia" ao início do array "lista".
lista.unshift("aveia");

// Imprime o array "lista" no console.
console.log(lista);


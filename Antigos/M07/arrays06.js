// Declaração de um array chamado "numeros" com os elementos 1, 2, 3 e 4.
let numeros = [1, 2, 3, 4];

// Utilizando a desestruturação de array para atribuir cada elemento do array "numeros" a uma variável correspondente.
let [a, b, c, d] = numeros;

// Imprime o valor da variável "a" no console.
console.log(a);

// Imprime o valor da variável "b" no console.
console.log(b);

// Imprime o valor da variável "c" no console.
console.log(c);

// Imprime o valor da variável "d" no console.
console.log(d);

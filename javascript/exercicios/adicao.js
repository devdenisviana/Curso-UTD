// adição de strings JavaScript

let text1 = "Jonh";
let text2 = "Doe";
let text3 = text1 + " " + text2;

console.log(text3);

//O operador de atribuição += também pode ser usado para adicionar (concatenar) strings:

let text4 = "What a very ";
text4 += "nice day";

console.log(text4);

